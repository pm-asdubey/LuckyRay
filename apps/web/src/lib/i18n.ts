// Utility functions
export function translatePlanet(name: string, lang: Language): string {
  if (lang === 'hi') return PLANET_HINDI[name] ?? name;
  return name;
}

export function translatePlanetAbbr(name: string, lang: Language): string {
  if (lang === 'hi') return PLANET_ABBR_HINDI[name] ?? name.slice(0, 2);
  return name.slice(0, 2);
}

export function translateSign(name: string, lang: Language): string {
  if (lang === 'hi') return SIGN_HINDI[name] ?? name;
  return name;
}

export function translateSignAbbr(name: string, lang: Language): string {
  if (lang === 'hi') return SIGN_ABBR_HINDI[name] ?? name.slice(0, 2);
  return name.slice(0, 3);
}

export function translateNakshatra(name: string, lang: Language): string {
  if (lang === 'hi') return NAKSHATRA_HINDI[name] ?? name;
  return name;
}

export function translateDignity(key: string, lang: Language): string {
  if (lang === 'hi') return DIGNITY_HINDI[key] ?? key;
  // English labels
  const EN: Record<string, string> = {
    Exalted: 'Exalted', Moolatrikona: 'Moolatrikona', OwnSign: 'Own sign',
    FriendlySign: 'Friendly', NeutralSign: 'Neutral', EnemySign: 'Enemy', Debilitated: 'Debilitated',
  };
  return EN[key] ?? key;
}

